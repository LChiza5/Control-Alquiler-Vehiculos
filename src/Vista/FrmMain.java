    private void abrirInternal(javax.swing.JInternalFrame f) {
    jDesktopPane1.add(f);
    f.setVisible(true);
    try {
        f.setIcon(false);
        f.setMaximum(false);   // por si estaba max
        f.setSelected(true);
    } catch (Exception ignore) {}

    // centrar
    Dimension d = jDesktopPane1.getSize();
    f.setLocation((d.width - f.getWidth())/2, (d.height - f.getHeight())/2);
    f.toFront();
}
    
